#Quebrando um número

from math import trunc
nume = float(input('Digite um número: '))
print(f'O valor inteiro fica {trunc(nume)}')

numero = float(input('Digite um número: '))
print(f'O número inteiro fica {numero:.0f}')

num = float(input('Digite um número: '))
print(f'A porção quebrada é {num} e a inteira é {int(num)}')

