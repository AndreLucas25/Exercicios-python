#Ler dois número e soma-los

n1 = int(input('Digite um número: ')) #Lê o primeiro número
n2 = int(input('Digite outro número: ')) #Lê o segundo número
soma = n1 + n2 #Váriavel que vai receber a soma entre os dois números lidos

print(f'{n1} + {n2} = {soma}') #Imprime o resultado da soma entre os dois números lidos